// Get random words for daily practice
export function getDailyWords(count: number = 10): Word[] {
  const shuffled = [...SAMPLE_WORDS].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

// Get words by category
export function getWordsByCategory(category: string): Word[] {
  return SAMPLE_WORDS.filter((word) => word.category === category);
}

// Get words by difficulty
export function getWordsByDifficulty(difficulty: string): Word[] {
  return SAMPLE_WORDS.filter((word) => word.difficulty === difficulty);
}

// Search words
export function searchWords(query: string): Word[] {
  const lowerQuery = query.toLowerCase();
  return SAMPLE_WORDS.filter(
    (word) =>
      word.term.toLowerCase().includes(lowerQuery) ||
      word.definition.toLowerCase().includes(lowerQuery) ||
      word.synonyms.some((syn) => syn.toLowerCase().includes(lowerQuery))
  );
}

// Get word by ID
export function getWordById(id: string): Word | undefined {
  return SAMPLE_WORDS.find((word) => word.id === id);
}
